#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	int		i;
	char	*res;

	i = 0;
	if (!s || f == NULL)
		return (NULL);
	if (!(res = ft_memalloc(ft_strlen(s) + 1)))
		return (NULL);
	res = ft_memalloc(ft_strlen(s) + 1);
	while (s[i] != '\0')
	{
		res[i] = f(s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
