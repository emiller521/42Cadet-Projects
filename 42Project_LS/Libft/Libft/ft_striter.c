#include "libft.h"

void		ft_striter(char *str, void (*f)(char *))
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (str != NULL && f != NULL)
	{
		i = ft_strlen(str);
		while (j < i)
		{
			(*f)(str);
			str++;
			j++;
		}
	}
}
